#mathematical operators

num=4
num2=7
print(num+num2)#sum
print(num-num2)#difference
print(num*num2)#product
print(num/num2)#quotient
print(num%num2)#remainder
print(num**num2)
print(num//num2)

#Comparison operators

num3=5
num4=9
print(num3 == num4) #compare
print(num3 != num4) #not equal
print(num3 > num4) #greater
print(num3 < num4) #less than
print(num3 >= num4) # greater or equal
print(num3 <= num4) #less or equal

